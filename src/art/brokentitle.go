func CreateASCIIArtBrokenTitle(text string) string {
	lines := make([]string, 5)
	for _, char := range strings.ToUpper(text) {
		if pattern, exists := bigBrokenChars[char]; exists {
			for i := 0; i < 5; i++ {
				lines[i] += pattern[i] + " "
			}
		}
	}

	return strings.Join(lines, "\n")
}
